package objects;

public class Post {
	
	public String letter;
	public int x, y, w, h, big, small;

	public Post() {
		
	}
	
	public String getLetter() {
		return letter;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int getW() {
		return w;
	}
	
	public int getH() {
		return h;
	}
	
	public int getBig() {
		return big;
	}
	
	public int getSmall() {
		return small;
	}

}